export function getThemeColors(style: UiStyleName): ThemeColors {
  return themes[style];
}

export function applyUiStyle(style: UiStyleName, isDark: boolean) {
  const colors = themes[style];
  const vars = isDark ? colors.dark : colors.light;
  const root = document.documentElement;
  
  Object.entries(vars).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });

  localStorage.setItem('ui-style', style);
}

export function getSavedUiStyle(): UiStyleName {
  const saved = localStorage.getItem('ui-style') as UiStyleName | null;
  return saved && saved in themes ? saved : 'warm';
}
